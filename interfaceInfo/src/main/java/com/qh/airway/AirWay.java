public class AirWay {
	
	private String airName;//航空公司名称
	private String simpleAirName;//航空公司简短名称
	private String airEname;//航空公司英文名称
	private String shortName;//字母简称
	public String getAirName() {
		return airName;
	}
	public void setAirName(String airName) {
		this.airName = airName;
	}
	public String getSimpleAirName() {
		return simpleAirName;
	}
	public void setSimpleAirName(String simpleAirName) {
		this.simpleAirName = simpleAirName;
	}
	public String getAirEname() {
		return airEname;
	}
	public void setAirEname(String airEname) {
		this.airEname = airEname;
	}
	public String getShortName() {
		return shortName;
	}
	public void setShortName(String shortName) {
		this.shortName = shortName;
	}
	
}
